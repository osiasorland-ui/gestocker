// Retourne les 12 départements
export const getDepartements = () => {
  return Object.keys(beninLocations);
};

// Retourne les communes d'un département
export const getCommunes = (departement) => {
  return beninLocations[departement]
    ? Object.keys(beninLocations[departement].communes)
    : [];
};

// Retourne les arrondissements d'une commune
export const getArrondissements = (departement, commune) => {
  return beninLocations[departement]?.communes[commune] || [];
};
